package com.tl.csv.trade;

public class CsvTrade {
	private String ulb;
	private String tradeSubType;
	private String tradeSubTypeCode;
	private LicenseType licenseType;
	private ApplicationType applicationType;
	private UOMUnit umo;
	private Float umoFrom;
	private Float umoTo;
	private Float licenseFee;
	public String getUlb() {
		return ulb;
	}
	public void setUlb(String ulb) {
		this.ulb = ulb;
	}
	public String getTradeSubType() {
		return tradeSubType;
	}
	public void setTradeSubType(String tradeSubType) {
		this.tradeSubType = tradeSubType;
	}
	public String getTradeSubTypeCode() {
		return tradeSubTypeCode;
	}
	public void setTradeSubTypeCode(String tradeSubTypeCode) {
		this.tradeSubTypeCode = tradeSubTypeCode;
	}
	public LicenseType getLicenseType() {
		return licenseType;
	}
	public void setLicenseType(LicenseType licenseType) {
		this.licenseType = licenseType;
	}
	public ApplicationType getApplicationType() {
		return applicationType;
	}
	public void setApplicationType(ApplicationType applicationType) {
		this.applicationType = applicationType;
	}
	public UOMUnit getUmo() {
		return umo;
	}
	public void setUmo(UOMUnit umo) {
		this.umo = umo;
	}
	public Float getUmoFrom() {
		return umoFrom;
	}
	public void setUmoFrom(Float umoFrom) {
		this.umoFrom = umoFrom;
	}
	public Float getUmoTo() {
		return umoTo;
	}
	public void setUmoTo(Float umoTo) {
		this.umoTo = umoTo;
	}
	public Float getLicenseFee() {
		return licenseFee;
	}
	public void setLicenseFee(Float licenseFee) {
		this.licenseFee = licenseFee;
	}
	
	
	
	
}
